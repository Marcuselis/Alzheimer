// Helper function to normalize molecule name
function normalizeMoleculeName(inputName) {
    if (!inputName) return null;
    const normalized = inputName.trim();
    // Try exact match first
    const exactMatch = MOLECULE_ALIAS_DATA.find(a => 
        a.source_intervention_name.toLowerCase() === normalized.toLowerCase()
    );
    if (exactMatch) return exactMatch.canonical_molecule;
    // Try canonical match
    if (typeof MOLECULE_REFERENCE_DATA !== "undefined") {
        const refMatch = MOLECULE_REFERENCE_DATA.find(m => 
            m.canonical_molecule.toLowerCase() === normalized.toLowerCase()
        );
        if (refMatch) return refMatch.canonical_molecule;
    }
    // Try partial match in aliases
    const partialMatch = MOLECULE_ALIAS_DATA.find(a => 
        a.source_intervention_name.toLowerCase().includes(normalized.toLowerCase()) ||
        normalized.toLowerCase().includes(a.source_intervention_name.toLowerCase())
    );
    if (partialMatch) return partialMatch.canonical_molecule;
    return normalized; // Return original if no match
}
