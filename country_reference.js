// Helper function to normalize country name
function normalizeCountryName(countryInput) {
    if (!countryInput) return null;
    const normalized = countryInput.trim();
    const match = COUNTRY_REFERENCE_DATA.find(c => 
        c.canonical_country.toLowerCase() === normalized.toLowerCase() ||
        normalized.toLowerCase().includes(c.canonical_country.toLowerCase())
    );
    return match ? match.canonical_country : normalized;
}

// Helper to check if country is Nordic/EU
function isNordicCountry(country) {
    const match = COUNTRY_REFERENCE_DATA.find(c => c.canonical_country === country);
    return match && match.is_nordic === "TRUE";
}
function isEUCountry(country) {
    const match = COUNTRY_REFERENCE_DATA.find(c => c.canonical_country === country);
    return match && match.is_eu === "TRUE";
}
